/**
 * /admin/pitch-generator — list / launcher view.
 *
 * Server-rendered list of recent PitchRun rows + a "New pitch" CTA. Filters
 * (status, prospect search) are applied via querystring to keep this page a
 * server component (no client fetch waterfall on first paint). Auth is
 * enforced by reading the staff session cookie directly. Anyone SALES_REP+
 * can see the list. ADMIN-only controls live on the detail page, not here.
 *
 * Reads PitchRun + Prospect via $queryRawUnsafe because both tables were
 * added late and downstream code paths still rely on raw access in places
 * (mirrors the pattern in /admin/builders/[id]).
 *
 * Feature flag: NEXT_PUBLIC_FEATURE_PITCH_GENERATOR_ENABLED. When unset or
 * 'false', renders a disabled-state block with link to .env.example.
 */

import Link from 'next/link'
import { redirect } from 'next/navigation'
import { Plus, Search, FileText, Sparkles } from 'lucide-react'
import { prisma } from '@/lib/prisma'
import { getStaffSession } from '@/lib/staff-auth'
import { parseRoles } from '@/lib/permissions'
import { formatDate } from '@/lib/utils'

export const dynamic = 'force-dynamic'
export const revalidate = 0

// Status values must match prisma/schema.prisma PitchRun.status
type PitchRunStatus =
  | 'QUEUED'
  | 'GENERATING'
  | 'PREVIEW'
  | 'APPROVED'
  | 'SENT'
  | 'FAILED'

const STATUS_VALUES: PitchRunStatus[] = [
  'QUEUED',
  'GENERATING',
  'PREVIEW',
  'APPROVED',
  'SENT',
  'FAILED',
]

interface PitchRunRow {
  id: string
  prospectId: string
  companyName: string
  style: string
  layout: string
  status: PitchRunStatus
  costEstimate: number | null
  generatedBy: string | null
  generatedByName: string | null
  createdAt: Date
}

interface SearchParams {
  status?: string
  q?: string
  page?: string
}

const PAGE_SIZE = 50

function statusBadgeClass(status: string): string {
  switch (status) {
    case 'APPROVED':
    case 'SENT':
      return 'bg-emerald-500/15 text-emerald-300 border-emerald-500/30'
    case 'PREVIEW':
      return 'bg-c1/15 text-c1 border-c1/30'
    case 'QUEUED':
    case 'GENERATING':
      return 'bg-amber-500/15 text-amber-300 border-amber-500/30'
    case 'FAILED':
      return 'bg-rose-500/15 text-rose-300 border-rose-500/30'
    default:
      return 'bg-white/10 text-fg-muted border-glass-border'
  }
}

function styleBadgeClass(style: string): string {
  switch (style) {
    case 'HERITAGE':
      return 'bg-amber-700/15 text-amber-200 border-amber-700/30'
    case 'EXECUTIVE':
      return 'bg-slate-500/15 text-slate-200 border-slate-500/30'
    case 'BUILDER_FIELD':
      return 'bg-orange-500/15 text-orange-200 border-orange-500/30'
    default:
      return 'bg-white/10 text-fg-muted border-glass-border'
  }
}

async function fetchPitchRuns(params: SearchParams): Promise<{
  rows: PitchRunRow[]
  total: number
}> {
  const conditions: string[] = []
  const args: unknown[] = []
  let idx = 1

  if (params.status && STATUS_VALUES.includes(params.status as PitchRunStatus)) {
    conditions.push(`pr."status" = $${idx}`)
    args.push(params.status)
    idx++
  }

  if (params.q && params.q.trim()) {
    conditions.push(`p."companyName" ILIKE $${idx}`)
    args.push(`%${params.q.trim()}%`)
    idx++
  }

  const where = conditions.length ? `WHERE ${conditions.join(' AND ')}` : ''
  const page = Math.max(1, parseInt(params.page || '1', 10) || 1)
  const offset = (page - 1) * PAGE_SIZE

  try {
    const countResult: Array<{ total: number }> = await prisma.$queryRawUnsafe(
      `SELECT COUNT(*)::int AS total
       FROM "PitchRun" pr
       JOIN "Prospect" p ON p."id" = pr."prospectId"
       ${where}`,
      ...args
    )
    const total = countResult[0]?.total ?? 0

    const rows: PitchRunRow[] = await prisma.$queryRawUnsafe(
      `SELECT
         pr."id",
         pr."prospectId",
         p."companyName",
         pr."style",
         pr."layout",
         pr."status",
         pr."costEstimate"::float AS "costEstimate",
         pr."generatedBy",
         (s."firstName" || ' ' || s."lastName") AS "generatedByName",
         pr."createdAt"
       FROM "PitchRun" pr
       JOIN "Prospect" p ON p."id" = pr."prospectId"
       LEFT JOIN "Staff" s ON s."id" = pr."generatedBy"
       ${where}
       ORDER BY pr."createdAt" DESC
       LIMIT ${PAGE_SIZE} OFFSET ${offset}`,
      ...args
    )

    return { rows, total }
  } catch {
    return { rows: [], total: 0 }
  }
}

